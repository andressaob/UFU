    private void visualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_visualizarActionPerformed
        Object[] possibleValues = usuarios.keySet().toArray();
        if((possibleValues != null)&&(possibleValues.length > 0)){
            Object selectedValue = JOptionPane.showInputDialog(this, "Escolha o usu√°rio", 
                    "Escolha", JOptionPane.INFORMATION_MESSAGE, null, possibleValues, 
                    possibleValues[0]);
            if(selectedValue != null){
                Cadastro c = new Cadastro(this, usuarios.get(selectedValue));
                c.setVisible(true);
            }else
                JOptionPane.showMessageDialog(this, "Nenhum usuario selecionado.");
        }
    }//GEN-LAST:event_visualizarActionPerformed

